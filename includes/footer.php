<script>
function handleSignup(e) {
    e.preventDefault();

    const form = e.target;

    if (!validateSignupForm(form)) {
        return;
    }

    const formData = new FormData(form);
    const submitBtn = form.querySelector('[type="submit"]');

    submitBtn.disabled = true;
    submitBtn.innerHTML = '<span class="spinner" style="width:18px;height:18px;border-width:2px;margin-right:8px;"></span> Creating Account...';

    fetch('<?php echo SITE_URL; ?>/pages/register.php', {
        method: 'POST',
        body: formData
    })
    .then(res => res.json())
    .then(data => {
        const container = document.getElementById('signupAlertContainer');

        if (data.success) {
            container.innerHTML = '<div class="alert alert-success"><svg viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2.5" stroke-linecap="round" stroke-linejoin="round" width="18" height="18"><polyline points="20 6 9 17 4 12"/></svg> ' + data.message + '</div>';

            form.reset();

            const passwordStrength = document.getElementById('passwordStrength');
            if (passwordStrength) {
                passwordStrength.style.display = 'none';
            }

            setTimeout(() => {
                window.location.href = '<?php echo SITE_URL; ?>/pages/login.php';
            }, 2000);
        } else {
            container.innerHTML = '<div class="alert alert-error"><svg viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2.5" stroke-linecap="round" stroke-linejoin="round" width="18" height="18"><line x1="18" y1="6" x2="6" y2="18"/><line x1="6" y1="6" x2="18" y2="18"/></svg> ' + data.message + '</div>';
        }
    })
    .catch(() => {
        document.getElementById('signupAlertContainer').innerHTML =
            '<div class="alert alert-error"><svg viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2.5" stroke-linecap="round" stroke-linejoin="round" width="18" height="18"><line x1="18" y1="6" x2="6" y2="18"/><line x1="6" y1="6" x2="18" y2="18"/></svg> An error occurred. Please try again.</div>';
    })
    .finally(() => {
        submitBtn.disabled = false;
        submitBtn.innerHTML = '<svg viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2" stroke-linecap="round" stroke-linejoin="round" width="18" height="18" style="margin-right:6px;"><path d="M16 21v-2a4 4 0 0 0-4-4H6a4 4 0 0 0-4 4v2"/><circle cx="9" cy="7" r="4"/><line x1="19" y1="8" x2="19" y2="14"/><line x1="22" y1="11" x2="16" y2="11"/></svg> Create Account';
    });
}
